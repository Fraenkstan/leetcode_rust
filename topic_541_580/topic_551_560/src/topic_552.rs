const M: i64 = 1000000007;

pub fn check_record(n: i32) -> i32 {
    let n = n as usize;
    let mut dp = vec![vec![vec![0; 3]; 2]; n + 1];
    dp[1][1][0] = 1_i64;
    dp[1][0][1] = 1_i64;
    dp[1][0][0] = 1_i64;

    for i in 2..=n {
        dp[i][0][0] = (dp[i - 1][0][0] + dp[i - 1][0][1] + dp[i - 1][0][2]) % M;
        dp[i][0][1] = dp[i - 1][0][0];
        dp[i][0][2] = dp[i - 1][0][1];

        dp[i][1][0] = (dp[i - 1][1][0] + dp[i - 1][1][1] + dp[i - 1][1][2]) % M
            + (dp[i - 1][0][0] + dp[i - 1][0][1] + dp[i - 1][0][2]) % M;
        dp[i][1][1] = dp[i - 1][1][0];
        dp[i][1][2] = dp[i - 1][1][1];
    }

    ((dp[n][0][0] + dp[n][0][1] + dp[n][1][0] + dp[n][1][1] + dp[n][0][2] + dp[n][1][2]) % M) as i32
}
